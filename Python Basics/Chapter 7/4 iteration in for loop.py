## For Loop with Lists
list1 = [1, 4, 6, 234, 6, 764]
print("\nList printed using for loop is : ")
for i in list1:
    print(i)

## For Loop with Tuples
tuple1 = (6, 231, 75, 122)
print("\nTuple printed using for loop is : ")
for i in tuple1:
    print(i)

## For Loop with Strings
string1 = "Hello World!"
print("\nString printed using for loop is : ")
for i in string1:
    print(i)